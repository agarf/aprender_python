A = 4
B = 3

# printf sem casa de precisao
print("resultado da divisao: {}".format(A / B)) 

# com duas casas de precisao.
print("resultado da divisao: {:.2f}".format(A / B))

# com tres casas de precisao.
print("resultado da divisao: {:.3f}".format(A / B)) 

# Nenhuma casa de precisao.Somente a parte inteira.
print("resultado da divisao: {:.0f}".format(A / B))

